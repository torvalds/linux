#include <errno.h>
#include <limits.h>
#include <stdio.h>
#include <string.h>
#include <unistd.h>

char *
getwd(char *buf)
{
	if (__getcwd(buf, PATH_MAX) == -1) {
		strlcpy(buf, strerror(errno), PATH_MAX);
		return NULL;
	}
	return buf;
}

__warn_references(getwd,
    "getwd() possibly used unsafely; consider using getcwd()");
