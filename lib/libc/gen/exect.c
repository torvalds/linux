#include <sys/cdefs.h>
__FBSDID("$FreeBSD$");

#include <sys/ptrace.h>

#include <errno.h>
#include <unistd.h>

int
exect(const char *path, char *const argv[], char *const envp[])
{

	if (ptrace(PT_TRACE_ME, 0, 0, 0) != 0) {
		if (errno != EBUSY)
			return (-1);
	}

	return (execve(path, argv, envp));
}
