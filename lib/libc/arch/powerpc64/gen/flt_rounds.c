#include <sys/types.h>
#include <float.h>
#include <ieeefp.h>


static const int map[] = {
	1,	/* round to nearest */
	0,	/* round to zero */
	2,	/* round to positive infinity */
	3	/* round to negative infinity */
};

int
__flt_rounds(void)
{
#ifdef _SOFT_FLOAT
	return map[fpgetround()];
#else
	double tmp;
	int x;

	__asm__ volatile("mffs %0; stfiwx %0,0,%1" : "=f"(tmp): "b"(&x));
	return map[x & 0x03];
#endif
}
DEF_STRONG(__flt_rounds);
