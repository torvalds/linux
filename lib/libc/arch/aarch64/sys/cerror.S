#include "SYS.h"

/* offsetof(struct tib, tib_errno) - offsetof(struct tib, __tib_tcb) */
#define TCB_OFFSET_ERRNO	(-12)

_ENTRY(CERROR)
	RETGUARD_SETUP(CERROR, x15)
	mrs	x1, tpidr_el0
	str	w0, [x1, #TCB_OFFSET_ERRNO]
	movn	x0, #0
	RETGUARD_CHECK(CERROR, x15)
	ret
END(CERROR)
