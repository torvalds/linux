#include "SYS.h"

WEAK_ALIAS(rindex, strrchr)

LEAF(strrchr, 0)
	.set	reorder
	move	v0, zero		# default if not found
	andi	a1, 0xff
1:
	lbu	a3, 0(a0)		# get a byte
	daddu	a0, a0, 1
	bne	a3, a1, 2f
	dsubu	v0, a0, 1		# save address of last match
2:
	bne	a3, zero, 1b		# continue if not end
	j	ra
END_STRONG(strrchr)
