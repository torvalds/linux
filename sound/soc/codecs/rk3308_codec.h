#define ACODEC_ADC_ANA_ALC_PGA			0x2c /* REG 0x0b */
/* Resevred REG 0x0c ~ 0x0f */

/* DAC ANALOG REGISTERS */
#define ACODEC_DAC_ANA_CTL0			0x00 /* REG 0x00 */
#define ACODEC_DAC_ANA_POP_VOLT			0x04 /* REG 0x01 */
