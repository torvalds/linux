/* Resevred REG 0x0f */

/* ADC ANALOG REGISTERS */
#define ACODEC_ADC_ANA_MIC_CTL			0x00 /* REG 0x00 */
#define ACODEC_ADC_ANA_MIC_GAIN			0x04 /* REG 0x01 */
#define ACODEC_ADC_ANA_ALC_CTL			0x08 /* REG 0x02 */
#define ACODEC_ADC_ANA_ALC_GAIN1		0x0c /* REG 0x03 */
