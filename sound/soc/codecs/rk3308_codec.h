#define ACODEC_DAC_ANA_HPMIX_CTL0		0x30 /* REG 0x0c */
#define ACODEC_DAC_ANA_HPMIX_CTL1		0x34 /* REG 0x0d */
#define ACODEC_DAC_ANA_LINEOUT_CTL0		0x38 /* REG 0x0e */
#define ACODEC_DAC_ANA_LINEOUT_CTL1		0x3c /* REG 0x0f */

