#define ACODEC_S_DAC_DATA_HI			0x24 /* REG 0x09 */
#define ACODEC_S_DAC_DATA_LO			0x28 /* REG 0x0a */
#define ACODEC_S_DAC_HPDET_DELAYTIME_HI		0x2c /* REG 0x0b */
#define ACODEC_S_DAC_HPDET_DELAYTIME_LO		0x30 /* REG 0x0c */
