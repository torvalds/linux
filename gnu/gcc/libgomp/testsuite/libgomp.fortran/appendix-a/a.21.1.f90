      SUBROUTINE WORK(K)
        INTEGER k
!$OMP ORDERED
        WRITE(*,*) K
!$OMP END ORDERED
      END SUBROUTINE WORK
      SUBROUTINE SUBA21(LB, UB, STRIDE)
        INTEGER LB, UB, STRIDE
        INTEGER I
!$OMP PARALLEL DO ORDERED SCHEDULE(DYNAMIC)
        DO I=LB,UB,STRIDE
          CALL WORK(I)
        END DO
!$OMP END PARALLEL DO
      END SUBROUTINE SUBA21
      PROGRAM A21
        CALL SUBA21(1,100,5)
      END PROGRAM A21
