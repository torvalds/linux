struct IntrinsicData {

  uint16_t Id;
  IntrinsicType Type;
  uint16_t Opc0;
  uint16_t Opc1;

  bool operator<(const IntrinsicData &RHS) const { return Id < RHS.Id; }
  bool operator==(const IntrinsicData &RHS) const { return RHS.Id == Id; }
  friend bool operator<(const IntrinsicData &LHS, unsigned Id) {
    return LHS.Id < Id;
  }
};

