#include <machine/asm.h>
#include "assym.inc"

	.text
	.set	noat
	.set	noreorder
	.section .text.mpentry_jz4780
	.balign 0x10000

/*
 * JZ4870 has stricter alignment requirement for
 * CPU entry point. Enforce it in CPU-specific
 * file.
 */
GLOBAL(jz4780_mpentry)
	j	mpentry
	nop
