#define MREGBIT_RECEIVE_IRQ_FRAME_COUNTER_MODE		BIT(30)
#define MREGBIT_RECEIVE_IRQ_MITIGATION_ENABLE		BIT(31)

/* MAC_GLOBAL_CONTROL (0x0100) */
