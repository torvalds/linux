#define EMAC_TX_DLINE_EN				BIT(16)
#define EMAC_TX_DLINE_STEP_MASK				GENMASK(21, 20)
#define EMAC_TX_DLINE_CODE_MASK				GENMASK(31, 24)

#define EMAC_DLINE_STEP_15P6				0  /* 15.6 ps/step */
