static int tianma_otm1906c_5p5_prepare(struct drm_panel *panel)
{
	struct tianma_otm1906c_5p5 *ctx = to_tianma_otm1906c_5p5(panel);
	struct device *dev = &ctx->dsi->dev;
	int ret;

	ret = regulator_bulk_enable(ARRAY_SIZE(tianma_otm1906c_5p5_supplies), ctx->supplies);
	if (ret < 0) {
		dev_err(dev, "Failed to enable regulators: %d\n", ret);
		return ret;
	}

	tianma_otm1906c_5p5_reset(ctx);

	ret = tianma_otm1906c_5p5_on(ctx);
	if (ret < 0) {
		dev_err(dev, "Failed to initialize panel: %d\n", ret);
		gpiod_set_value_cansleep(ctx->reset_gpio, 1);
		regulator_bulk_disable(ARRAY_SIZE(tianma_otm1906c_5p5_supplies), ctx->supplies);
		return ret;
	}

	return 0;
}

static int tianma_otm1906c_5p5_unprepare(struct drm_panel *panel)
{
	struct tianma_otm1906c_5p5 *ctx = to_tianma_otm1906c_5p5(panel);
	struct device *dev = &ctx->dsi->dev;
	int ret;

	ret = tianma_otm1906c_5p5_off(ctx);
	if (ret < 0)
		dev_err(dev, "Failed to un-initialize panel: %d\n", ret);

	gpiod_set_value_cansleep(ctx->reset_gpio, 1);
	regulator_bulk_disable(ARRAY_SIZE(tianma_otm1906c_5p5_supplies), ctx->supplies);

	return 0;
}

