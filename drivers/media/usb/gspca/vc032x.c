static void setsharpness(struct gspca_dev *gspca_dev, s32 val)
{
	struct sd *sd = (struct sd *) gspca_dev;
	u8 data;

	switch (sd->sensor) {
	case SENSOR_PO1200:
		data = 0;
		i2c_write(gspca_dev, 0x03, &data, 1);
		if (val < 0)
			data = 0x6a;
		else
			data = 0xb5 + val * 3;
		i2c_write(gspca_dev, 0x61, &data, 1);
		break;
	case SENSOR_POxxxx:
		if (val < 0)
			data = 0x7e;	/* def = max */
		else
			data = 0x60 + val * 0x0f;
		i2c_write(gspca_dev, 0x59, &data, 1);
		break;
	}
}
