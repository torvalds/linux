static void ov2775_get_lcg_reg(u32 gain, u32 *again_reg, u32 *dgain_reg)
{
	u32 again;
	u32 dgain;

	/* LCG,min A_gain is 2x,min D_gain is 1.09375x,min T_Gain is 3x */
	if (gain < 0x300) {
		/* T_Gain < 3, error */
		*again_reg = 0x01;
		*dgain_reg = 0x118;
	} else if (gain < 0x460) {
		/* A_gain is 2x */
		again = 0x200;
		*again_reg = 0x01;
	} else if (gain < 0x8c0) {
		/* T_Gain >= 4.375x, A_gain is 4x */
		again = 0x400;
		*again_reg = 0x02;
	} else {
		/* T_Gain >= 8.75x, A_gain is 8x */
		again = 0x800;
		*again_reg = 0x03;
	}

	if (gain >= 0x300) {
		dgain = (gain * 0x100 + 0x80) / again;
		if (dgain < 0x118)
			dgain = 0x118;

		*dgain_reg = dgain;
	}
}

static void ov2775_get_hcg_reg(u32 gain, u32 *again_reg, u32 *dgain_reg)
{
	u32 again;
	u32 dgain;

	/* HCG,T_Gain >= 22x,min A_gain is 1x,min D_gain is 2.0x */
	if (gain < 0x200) {
		/* T_Gain < 22, error */
		*again_reg = 0x00;
		*dgain_reg = 0x200;
	} else if (gain < 0x400) {
		/* T_Gain >= 22x, A_gain is 1x */
		again = 0x100;
		*again_reg = 0x00;
	} else if (gain < 0x800) {
		/* T_Gain >= 44x, A_gain is 2x */
		again = 0x200;
		*again_reg = 0x01;
	} else if (gain < 0x1000) {
		/* T_Gain >= 88x, A_gain is 4x */
		again = 0x400;
		*again_reg = 0x02;
	} else {
		/* T_Gain >= 176x, A_gain is 8x */
		again = 0x800;
		*again_reg = 0x03;
	}

	if (gain >= 0x200) {
		dgain = (gain * 0x100 + 0x80) / again;
		if (dgain < 0x200)
			dgain = 0x200;

		*dgain_reg = dgain;
	}
}

static int ov2775_g_mbus_config(struct v4l2_subdev *sd,
				struct v4l2_mbus_config *config)
{
	struct ov2775 *ov2775 = to_ov2775(sd);
	const struct ov2775_mode *mode = ov2775->cur_mode;
	u32 val = 0;

	if (mode->hdr_mode == NO_HDR)
		val = 1 << (mode->lane - 1) |
		V4L2_MBUS_CSI2_CHANNEL_0 |
		V4L2_MBUS_CSI2_CONTINUOUS_CLOCK;
	if (mode->hdr_mode == HDR_X2)
		val = 1 << (mode->lane - 1) |
		V4L2_MBUS_CSI2_CHANNEL_0 |
		V4L2_MBUS_CSI2_CONTINUOUS_CLOCK |
		V4L2_MBUS_CSI2_CHANNEL_1;

	config->type = V4L2_MBUS_CSI2;
	config->flags = val;

	return 0;
}

