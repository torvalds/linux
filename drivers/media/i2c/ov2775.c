static int ov2775_write_reg(struct i2c_client *client, u16 reg,
			    u32 len, u32 val)
{
	int ret = -1;
	struct i2c_msg msg[1];
	unsigned char data[5];
	int retries;

	for (retries = 0; retries < 5; retries++) {
		msg->addr = client->addr;
		msg->flags = 0;
		msg->len = 3;
		msg->buf = data;

		/* high byte goes out first */
		data[0] = (u8)(reg >> 8);
		data[1] = (u8)(reg & 0xff);

		switch (len) {
		case OV2775_REG_VALUE_08BIT:
			data[2] = val;
			break;
		case OV2775_REG_VALUE_16BIT:
			data[2] = (u8)(val >> 8);
			data[3] = (u8)(val & 0xff);
			break;
		case OV2775_REG_VALUE_24BIT:
			data[2] = (u8)(val >> 16);
			data[3] = (u8)(val >> 8);
			data[3] = (u8)(val & 0xff);
			break;
		}

		ret = i2c_transfer(client->adapter, msg, 1);
		if (ret == 1)
			return 0;

		set_current_state(TASK_UNINTERRUPTIBLE);
		schedule_timeout(msecs_to_jiffies(20));
	}

	return ret;
}

static int ov2775_write_array(struct i2c_client *client,
			      const struct regval *regs)
{
	u32 i;
	int ret = 0;

	for (i = 0; ret == 0 && regs[i].addr != REG_NULL; i++) {
		if (unlikely(regs[i].addr == REG_DELAY))
			usleep_range(regs[i].val, regs[i].val * 2);
		else
			ret = ov2775_write_reg(client, regs[i].addr,
					       OV2775_REG_VALUE_08BIT,
					       regs[i].val);
	}

	return ret;
}

static int ov2775_read_reg(struct i2c_client *client, u16 reg,
			   u32 len, u32 *val)
{
	int ret = 0;
	struct i2c_msg msg[1];
	unsigned char data[4] = { 0, 0, 0, 0 };

	msg->addr = client->addr;
	msg->flags = 0;
	msg->buf = data;

	/* High byte goes out first */
	data[0] = (u8)(reg >> 8);
	data[1] = (u8)(reg & 0xff);
	msg->len = 2;
	ret = i2c_transfer(client->adapter, msg, 1);
	if (ret >= 0) {
		usleep_range(3000, 4000);
		msg->flags = I2C_M_RD;
		msg->len = len;
		i2c_transfer(client->adapter, msg, 1);
	}
	if (ret >= 0) {
		*val = 0;
		/* High byte comes first */
		if (len == 1)
			*val = data[0];
		else if (len == 2)
			*val = data[1] + (data[0] << 8);
		else
			*val = data[3] + (data[2] << 8) +
			    (data[1] << 16) + (data[0] << 24);
		return 0;
	}

	return ret;
}

static int ov2775_get_reso_dist(const struct ov2775_mode *mode,
				struct v4l2_mbus_framefmt *framefmt)
{
	return abs(mode->width - framefmt->width) +
	       abs(mode->height - framefmt->height);
}

static const struct ov2775_mode *
ov2775_find_best_fit(struct v4l2_subdev *sd,
		     struct v4l2_subdev_format *fmt)
{
	struct ov2775 *ov2775 = to_ov2775(sd);
	struct v4l2_mbus_framefmt *framefmt = &fmt->format;
	int dist;
	int cur_best_fit = 0;
	int cur_best_fit_dist = -1;
	unsigned int i;

	for (i = 0; i < ov2775->support_modes_num; i++) {
		dist = ov2775_get_reso_dist(&ov2775->support_modes[i],
					    framefmt);
		if (cur_best_fit_dist == -1 || dist < cur_best_fit_dist) {
			cur_best_fit_dist = dist;
			cur_best_fit = i;
		}
	}

	return &ov2775->support_modes[cur_best_fit];
}

static int ov2775_set_fmt(struct v4l2_subdev *sd,
			  struct v4l2_subdev_pad_config *cfg,
			  struct v4l2_subdev_format *fmt)
{
	struct ov2775 *ov2775 = to_ov2775(sd);
	const struct ov2775_mode *mode;
	s64 h_blank, vblank_def;

	mutex_lock(&ov2775->mutex);

	mode = ov2775_find_best_fit(sd, fmt);
	fmt->format.code = mode->bus_fmt;
	fmt->format.width = mode->width;
	fmt->format.height = mode->height;
	fmt->format.field = V4L2_FIELD_NONE;
	if (fmt->which == V4L2_SUBDEV_FORMAT_TRY) {
#ifdef CONFIG_VIDEO_V4L2_SUBDEV_API
		*v4l2_subdev_get_try_format(sd, cfg, fmt->pad) = fmt->format;
#else
		mutex_unlock(&ov2775->mutex);
		return -ENOTTY;
#endif
	} else {
		ov2775->cur_mode = mode;
		h_blank = mode->hts_def - mode->width;
		__v4l2_ctrl_modify_range(ov2775->hblank, h_blank,
					 h_blank, 1, h_blank);
		vblank_def = mode->vts_def - mode->height;
		__v4l2_ctrl_modify_range(ov2775->vblank, vblank_def,
					 OV2775_VTS_MAX - mode->height,
					 1, vblank_def);
	}

	mutex_unlock(&ov2775->mutex);

	return 0;
}

static int ov2775_get_fmt(struct v4l2_subdev *sd,
			  struct v4l2_subdev_pad_config *cfg,
			  struct v4l2_subdev_format *fmt)
{
	struct ov2775 *ov2775 = to_ov2775(sd);
	const struct ov2775_mode *mode = ov2775->cur_mode;

	mutex_lock(&ov2775->mutex);
	if (fmt->which == V4L2_SUBDEV_FORMAT_TRY) {
#ifdef CONFIG_VIDEO_V4L2_SUBDEV_API
		fmt->format = *v4l2_subdev_get_try_format(sd, cfg, fmt->pad);
#else
		mutex_unlock(&ov2775->mutex);
		return -ENOTTY;
#endif
	} else {
		fmt->format.width = mode->width;
		fmt->format.height = mode->height;
		fmt->format.code = mode->bus_fmt;
		fmt->format.field = V4L2_FIELD_NONE;
		if (fmt->pad < PAD_MAX && mode->hdr_mode != NO_HDR)
			fmt->reserved[0] = mode->vc[fmt->pad];
		else
			fmt->reserved[0] = mode->vc[PAD0];
	}
	mutex_unlock(&ov2775->mutex);

	return 0;
}

