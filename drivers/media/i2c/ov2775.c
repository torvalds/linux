static int ov2775_set_flip(struct ov2775 *ov2775, u32 flip)
{
	u32 mode = 0;
	int ret = 0;

	ret = ov2775_read_reg(ov2775->client, OV2775_READ_MODE,
			      OV2775_REG_VALUE_08BIT, &mode);

	mode &= ~(OV2775_MIRROR | OV2775_FLIP);
	mode |= flip;

	ret |= ov2775_write_reg(ov2775->client,
				OV2775_REG_OPERATE_CTL,
				OV2775_REG_VALUE_08BIT,
				0x00);
	if (flip & OV2775_MIRROR) {
		ret |= ov2775_write_reg(ov2775->client,
					0x30a9 | OV2775_REG_GROUP_BIT,
					OV2775_REG_VALUE_08BIT, 0x3);
		ret |= ov2775_write_reg(ov2775->client,
					0x3252 | OV2775_REG_GROUP_BIT,
					OV2775_REG_VALUE_08BIT, 0x21);
	} else {
		ret |= ov2775_write_reg(ov2775->client,
					0x30a9 | OV2775_REG_GROUP_BIT,
					OV2775_REG_VALUE_08BIT, 0x4);
		ret |= ov2775_write_reg(ov2775->client,
					0x3252 | OV2775_REG_GROUP_BIT,
					OV2775_REG_VALUE_08BIT, 0x20);
	}

	ret |= ov2775_write_reg(ov2775->client,
				OV2775_READ_MODE | OV2775_REG_GROUP_BIT,
				OV2775_REG_VALUE_08BIT, mode);
	/* launch immediately */
	ret |= ov2775_write_reg(ov2775->client,
				OV2775_REG_GROUP_CTL,
				OV2775_REG_VALUE_08BIT,
				0x20);

	/* single start */
	ret |= ov2775_write_reg(ov2775->client,
				OV2775_REG_OPERATE_CTL,
				OV2775_REG_VALUE_08BIT,
				0x01);

	return ret;
}

static int __ov2775_start_stream(struct ov2775 *ov2775)
{
	int ret;

	ret = ov2775_write_array(ov2775->client,
				 ov2775->cur_mode->reg_list);
	if (ret)
		return ret;

	ret = __v4l2_ctrl_handler_setup(&ov2775->ctrl_handler);
	/* In case these controls are set before streaming */
	if (ov2775->has_init_exp && ov2775->cur_mode->hdr_mode != NO_HDR) {
		ret = ov2775_ioctl(&ov2775->subdev, PREISP_CMD_SET_HDRAE_EXP,
				   &ov2775->init_hdrae_exp);
		if (ret) {
			dev_err(&ov2775->client->dev,
				"init exp fail in hdr mode\n");
			return ret;
		}
	}

	return ov2775_write_reg(ov2775->client, OV2775_REG_CTRL_MODE,
				OV2775_REG_VALUE_08BIT, OV2775_MODE_STREAMING);
}

static int __ov2775_stop_stream(struct ov2775 *ov2775)
{
	ov2775->has_init_exp = false;
	return ov2775_write_reg(ov2775->client, OV2775_REG_CTRL_MODE,
				OV2775_REG_VALUE_08BIT, OV2775_MODE_SW_STANDBY);
}

static int ov2775_s_stream(struct v4l2_subdev *sd, int on)
{
	struct ov2775 *ov2775 = to_ov2775(sd);
	struct i2c_client *client = ov2775->client;
	int ret = 0;

	mutex_lock(&ov2775->mutex);
	on = !!on;
	if (on == ov2775->streaming)
		goto unlock_and_return;

	if (on) {
		ret = pm_runtime_get_sync(&client->dev);
		if (ret < 0) {
			pm_runtime_put_noidle(&client->dev);
			goto unlock_and_return;
		}

		ret = __ov2775_start_stream(ov2775);
		if (ret) {
			v4l2_err(sd, "start stream failed while write regs\n");
			pm_runtime_put(&client->dev);
			goto unlock_and_return;
		}
	} else {
		__ov2775_stop_stream(ov2775);
		pm_runtime_put(&client->dev);
	}

	ov2775->streaming = on;

unlock_and_return:
	mutex_unlock(&ov2775->mutex);

	return ret;
}

static int ov2775_s_power(struct v4l2_subdev *sd, int on)
{
	struct ov2775 *ov2775 = to_ov2775(sd);
	struct i2c_client *client = ov2775->client;
	int ret = 0;

	mutex_lock(&ov2775->mutex);

	/* If the power state is not modified - no work to do. */
	if (ov2775->power_on == !!on)
		goto unlock_and_return;

	if (on) {
		ret = pm_runtime_get_sync(&client->dev);
		if (ret < 0) {
			pm_runtime_put_noidle(&client->dev);
			goto unlock_and_return;
		}

		ov2775->power_on = true;
	} else {
		pm_runtime_put(&client->dev);
		ov2775->power_on = false;
	}

unlock_and_return:
	mutex_unlock(&ov2775->mutex);

	return ret;
}

/* Calculate the delay in us by clock rate and clock cycles */
static inline u32 ov2775_cal_delay(u32 cycles)
{
	return DIV_ROUND_UP(cycles, OV2775_XVCLK_FREQ / 1000 / 1000);
}

static int __ov2775_power_on(struct ov2775 *ov2775)
{
	int ret;
	u32 delay_us;
	struct device *dev = &ov2775->client->dev;

	if (!IS_ERR_OR_NULL(ov2775->pins_default)) {
		ret = pinctrl_select_state(ov2775->pinctrl,
					   ov2775->pins_default);
		if (ret < 0)
			dev_err(dev, "could not set pins\n");
	}
	ret = clk_set_rate(ov2775->xvclk, OV2775_XVCLK_FREQ);
	if (ret < 0)
		dev_warn(dev, "Failed to set xvclk rate (24MHz)\n");
	if (clk_get_rate(ov2775->xvclk) != OV2775_XVCLK_FREQ)
		dev_warn(dev, "xvclk mismatched, modes are based on 24MHz\n");
	ret = clk_prepare_enable(ov2775->xvclk);
	if (ret < 0) {
		dev_err(dev, "Failed to enable xvclk\n");
		return ret;
	}
	ret = regulator_bulk_enable(OV2775_NUM_SUPPLIES, ov2775->supplies);
	if (ret < 0) {
		dev_err(dev, "Failed to enable regulators\n");
		goto disable_clk;
	}
	usleep_range(5000, 5100);
	if (!IS_ERR(ov2775->pwd_gpio)) {
		gpiod_direction_output(ov2775->pwd_gpio, 0);
		usleep_range(100, 200);
	}

	if (!IS_ERR(ov2775->pd_gpio)) {
		gpiod_direction_output(ov2775->pd_gpio, 1);
		usleep_range(100, 200);
	}

	if (!IS_ERR(ov2775->rst_gpio)) {
		gpiod_direction_output(ov2775->rst_gpio, 1);
		usleep_range(100, 200);
		gpiod_direction_output(ov2775->rst_gpio, 0);
	}
	/* 8192 cycles prior to first SCCB transaction */
	delay_us = ov2775_cal_delay(8192);
	usleep_range(delay_us * 2, delay_us * 4);

	return 0;

disable_clk:
	clk_disable_unprepare(ov2775->xvclk);

	return ret;
}

static void __ov2775_power_off(struct ov2775 *ov2775)
{
	int ret;
	struct device *dev = &ov2775->client->dev;

	if (!IS_ERR(ov2775->pd_gpio))
		gpiod_direction_output(ov2775->pd_gpio, 0);

	clk_disable_unprepare(ov2775->xvclk);

	if (!IS_ERR(ov2775->rst_gpio))
		gpiod_direction_output(ov2775->rst_gpio, 1);

	if (!IS_ERR(ov2775->pwd_gpio))
		gpiod_direction_output(ov2775->pwd_gpio, 1);

	if (!IS_ERR_OR_NULL(ov2775->pins_sleep)) {
		ret = pinctrl_select_state(ov2775->pinctrl,
					   ov2775->pins_sleep);
		if (ret < 0)
			dev_err(dev, "could not set pins\n");
	}

	regulator_bulk_disable(OV2775_NUM_SUPPLIES, ov2775->supplies);
}

static int ov2775_runtime_resume(struct device *dev)
{
	struct i2c_client *client = to_i2c_client(dev);
	struct v4l2_subdev *sd = i2c_get_clientdata(client);
	struct ov2775 *ov2775 = to_ov2775(sd);

	return __ov2775_power_on(ov2775);
}

static int ov2775_runtime_suspend(struct device *dev)
{
	struct i2c_client *client = to_i2c_client(dev);
	struct v4l2_subdev *sd = i2c_get_clientdata(client);
	struct ov2775 *ov2775 = to_ov2775(sd);

	__ov2775_power_off(ov2775);

	return 0;
}

