static long ov2775_ioctl(struct v4l2_subdev *sd, unsigned int cmd, void *arg)
{
	struct ov2775 *ov2775 = to_ov2775(sd);
	struct i2c_client *client = ov2775->client;
	struct preisp_hdrae_exp_s *hdrae_exp = arg;
	struct rkmodule_hdr_cfg *hdr_cfg;
	u32 s_again, s_dgain, l_again, l_dgain;
	u32 again, i, h, w;
	long ret = 0;
	u32 stream = 0;

	switch (cmd) {
	case PREISP_CMD_SET_HDRAE_EXP:
		if (!ov2775->has_init_exp && !ov2775->streaming) {
			ov2775->init_hdrae_exp = *hdrae_exp;
			ov2775->has_init_exp = true;
			dev_info(&client->dev, "ov2775 don't streaming, record exp for hdr!\n");
			break;
		}
		dev_dbg(&client->dev,
			"rev exp req: L_exp: 0x%x, 0x%x, S_exp: 0x%x, 0x%x\n",
			hdrae_exp->middle_exp_reg,
			hdrae_exp->middle_gain_reg,
			hdrae_exp->short_exp_reg,
			hdrae_exp->short_gain_reg);

		ov2775_get_hcg_reg(hdrae_exp->middle_gain_reg,
				   &l_again,
				   &l_dgain);
		ov2775_get_lcg_reg(hdrae_exp->short_gain_reg,
				   &s_again,
				   &s_dgain);

		again = s_again << 2 | l_again;

		ret = ov2775_write_reg(ov2775->client,
				       OV2775_REG_OPERATE_CTL,
				       OV2775_REG_VALUE_08BIT,
				       0x00);

		/* group 0 */
		ret |= ov2775_write_reg(ov2775->client,
					OV2775_REG_GROUP_CTL,
					OV2775_REG_VALUE_08BIT,
					0x00);

		ret |= ov2775_write_reg(ov2775->client,
					OV2775_REG_EXPOSURE_H |
					OV2775_REG_GROUP_BIT,
					OV2775_REG_VALUE_08BIT,
					(hdrae_exp->middle_exp_reg >> 8) & 0xFF);

		ret |= ov2775_write_reg(ov2775->client,
					OV2775_REG_EXPOSURE_L |
					OV2775_REG_GROUP_BIT,
					OV2775_REG_VALUE_08BIT,
					hdrae_exp->middle_exp_reg & 0xFF);

		ret |= ov2775_write_reg(ov2775->client,
					OV2775_REG_GAIN |
					OV2775_REG_GROUP_BIT,
					OV2775_REG_VALUE_08BIT,
					again);

		ret |= ov2775_write_reg(ov2775->client,
					OV2775_REG_DIG_GAIN_HCG_H |
					OV2775_REG_GROUP_BIT,
					OV2775_REG_VALUE_08BIT,
					(l_dgain >> 8) & 0xFF);

		ret |= ov2775_write_reg(ov2775->client,
					OV2775_REG_DIG_GAIN_HCG_L |
					OV2775_REG_GROUP_BIT,
					OV2775_REG_VALUE_08BIT,
					l_dgain & 0xFF);

		ret |= ov2775_write_reg(ov2775->client,
					OV2775_REG_DIG_GAIN_LCG_H |
					OV2775_REG_GROUP_BIT,
					OV2775_REG_VALUE_08BIT,
					(s_dgain >> 8) & 0xFF);

		ret |= ov2775_write_reg(ov2775->client,
					OV2775_REG_DIG_GAIN_LCG_L |
					OV2775_REG_GROUP_BIT,
					OV2775_REG_VALUE_08BIT,
					s_dgain & 0xFF);

		/* launch immediately */
		ret |= ov2775_write_reg(ov2775->client,
					OV2775_REG_GROUP_CTL,
					OV2775_REG_VALUE_08BIT,
					0x40);

		/* single start */
		ret |= ov2775_write_reg(ov2775->client,
					OV2775_REG_OPERATE_CTL,
					OV2775_REG_VALUE_08BIT,
					0x01);

		dev_dbg(&client->dev, "set to reg, exp: 0x%02x, again: 0x%02x, l_dgain: 0x%02x, s_dgain: 0x%02x\n",
			hdrae_exp->middle_exp_reg,
			again,
			l_dgain,
			s_dgain);
		break;
	case RKMODULE_GET_HDR_CFG:
		hdr_cfg = (struct rkmodule_hdr_cfg *)arg;
		hdr_cfg->esp.mode = HDR_NORMAL_VC;
		hdr_cfg->hdr_mode = ov2775->cur_mode->hdr_mode;
		break;
	case RKMODULE_GET_MODULE_INFO:
		ov2775_get_module_inf(ov2775, (struct rkmodule_inf *)arg);
		break;
	case RKMODULE_SET_HDR_CFG:
		hdr_cfg = (struct rkmodule_hdr_cfg *)arg;
		w = ov2775->cur_mode->width;
		h = ov2775->cur_mode->height;
		for (i = 0; i < ov2775->support_modes_num; i++) {
			if (w == supported_modes[i].width &&
			h == supported_modes[i].height &&
			supported_modes[i].hdr_mode == hdr_cfg->hdr_mode) {
				ov2775->cur_mode = &supported_modes[i];
				break;
			}
		}
		if (i == ov2775->support_modes_num) {
			dev_err(&client->dev,
				"not find hdr mode:%d %dx%d config\n",
				hdr_cfg->hdr_mode, w, h);
			ret = -EINVAL;
		} else {
			w = ov2775->cur_mode->hts_def - ov2775->cur_mode->width;
			h = ov2775->cur_mode->vts_def - ov2775->cur_mode->height;
			__v4l2_ctrl_modify_range(ov2775->hblank, w, w, 1, w);
			__v4l2_ctrl_modify_range(ov2775->vblank, h,
				OV2775_VTS_MAX - ov2775->cur_mode->height,
				1, h);

			dev_info(&client->dev,
				"sensor hdr mode: %d, supported_modes[%d]\n",
				ov2775->cur_mode->hdr_mode, i);
		}
		break;
	case RKMODULE_SET_QUICK_STREAM:

		stream = *((u32 *)arg);

		if (stream)
			ret = ov2775_write_reg(ov2775->client, OV2775_REG_CTRL_MODE,
				OV2775_REG_VALUE_08BIT, OV2775_MODE_STREAMING);
		else
			ret = ov2775_write_reg(ov2775->client, OV2775_REG_CTRL_MODE,
				OV2775_REG_VALUE_08BIT, OV2775_MODE_SW_STANDBY);
		break;
	default:
		return -ENOIOCTLCMD;
	}

	return ret;
}

