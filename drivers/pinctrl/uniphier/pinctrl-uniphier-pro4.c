static int uniphier_pro4_get_gpio_muxval(unsigned int pin,
					 unsigned int gpio_offset)
{
	if (gpio_offset >= 134 && gpio_offset <= 140)	/* XIRQ14-20 */
		return 2;

	return 7;
}

