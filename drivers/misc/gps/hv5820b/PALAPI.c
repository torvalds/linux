///////////////////////////////////////////////////////////////////////////////////
// 
// Function Name:PAL_GetCurTimer_US
// Parameters:
// Description: Get system time 
// Notes: sjchen 2010/11/30
//
///////////////////////////////////////////////////////////////////////////////////
void PAL_GetCurTimer_US(
					  int *n32Second,
					  int *n32us
	)
{
	struct timeval  tv;

	do_gettimeofday(&tv);

	*n32Second = tv.tv_sec;
	*n32us     = tv.tv_usec;
}


