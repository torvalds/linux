#include "baselocl.h"

struct heim_type_data _heim_bool_object = {
    HEIM_TID_BOOL,
    "bool-object",
    NULL,
    NULL,
    NULL,
    NULL,
    NULL
};

heim_bool_t
heim_bool_create(int val)
{
    return heim_base_make_tagged_object(!!val, HEIM_TID_BOOL);
}

int
heim_bool_val(heim_bool_t ptr)
{
    return heim_base_tagged_object_value(ptr);
}
